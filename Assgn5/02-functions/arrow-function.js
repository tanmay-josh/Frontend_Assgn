// Arrow function to add two numbers

const addArrow = (a, b) => {
  return a + b;
};

// Can also be written in one line
const addArrowOneLine = (a, b) => a + b;

console.log("Sum of 5 and 3:", addArrow(5, 3)); // 8
console.log("Sum of 10 and 20:", addArrow(10, 20)); // 30
console.log("Sum of -5 and 10:", addArrowOneLine(-5, 10)); // 5
